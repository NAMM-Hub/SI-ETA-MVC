document.addEventListener('DOMContentLoaded', function() {
    const passwordField = document.getElementById('nueva_contrasena');
    const showPasswordCheckbox = document.getElementById('showPassword1');
    const togglePasswordIcon = document.getElementById('togglePassword'); // Para la opción de icono

    // Opción con Checkbox:
    if (showPasswordCheckbox) {
        showPasswordCheckbox.addEventListener('change', function() {
            if (this.checked) {
                passwordField.type = 'text';
            } else {
                passwordField.type = 'password';
            }
        });
    }


});

